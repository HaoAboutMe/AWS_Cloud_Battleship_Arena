const LanguageContext = createContext(null);

function resolveTranslation(language, key, values = {}) {
  const value = key.split(".").reduce((current, part) => current?.[part], translations[language]);
  const fallback = key.split(".").reduce((current, part) => current?.[part], translations.en);
  const text = value || fallback || key;

  return Object.entries(values).reduce(
    (result, [name, replacement]) => result.replaceAll(`{${name}}`, replacement),
    text,
  );
}

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(() => localStorage.getItem("battleshipLanguage") || "en");

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo(() => ({
    language,
    setLanguage(nextLanguage) {
      if (nextLanguage === language) return;

      localStorage.setItem("battleshipLanguage", nextLanguage);
      document.documentElement.lang = nextLanguage;
      setLanguageState(nextLanguage);
    },
    t(key, values) {
      return resolveTranslation(language, key, values);
    },
  }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

// The hook intentionally shares this module with its provider.
// eslint-disable-next-line react-refresh/only-export-components
export function useLanguage() {
  return useContext(LanguageContext);
}
